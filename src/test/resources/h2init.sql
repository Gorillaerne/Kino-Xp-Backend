-- Drop tables in dependency order (reverse order of creation)
DROP TABLE IF EXISTS booked_seats;
DROP TABLE IF EXISTS ticket;
DROP TABLE IF EXISTS reservation;
DROP TABLE IF EXISTS shows;
DROP TABLE IF EXISTS movie;
DROP TABLE IF EXISTS seat;
DROP TABLE IF EXISTS theatre;
DROP TABLE IF EXISTS cinema_user;
DROP TABLE IF EXISTS cinema;
DROP TABLE IF EXISTS user; -- Added

-- Create Tables (Basic DDL assuming data types based on inserts)
CREATE TABLE IF NOT EXISTS cinema (
                                      id INT PRIMARY KEY,
                                      name VARCHAR(255) NOT NULL
    );

CREATE TABLE IF NOT EXISTS user ( -- Added
                                    id INT PRIMARY KEY,
                                    username VARCHAR(50) NOT NULL,
    email VARCHAR(255) UNIQUE
    );

CREATE TABLE IF NOT EXISTS cinema_user (
                                           cinema_id INT NOT NULL,
                                           user_id INT NOT NULL,
                                           PRIMARY KEY (cinema_id, user_id),
    FOREIGN KEY (cinema_id) REFERENCES cinema(id),
    FOREIGN KEY (user_id) REFERENCES user(id) -- Added FK reference
    );

CREATE TABLE IF NOT EXISTS theatre (
                                       id INT PRIMARY KEY,
                                       name VARCHAR(255) NOT NULL,
    cinema_id INT NOT NULL,
    FOREIGN KEY (cinema_id) REFERENCES cinema(id)
    );

CREATE TABLE IF NOT EXISTS seat (
                                    id INT PRIMARY KEY,
                                    seat_row INT NOT NULL,
                                    seat_number INT NOT NULL,
                                    theatre_id INT NOT NULL,
                                    FOREIGN KEY (theatre_id) REFERENCES theatre(id)
    );

CREATE TABLE IF NOT EXISTS movie (
                                     id INT PRIMARY KEY,
                                     title VARCHAR(255) NOT NULL,
    duration TIME NOT NULL,
    description VARCHAR(255),
    poster_image VARCHAR(512),
    category VARCHAR(50),
    age_limit INT
    );

CREATE TABLE IF NOT EXISTS shows (
                                     id INT PRIMARY KEY,
                                     show_time TIMESTAMP NOT NULL,
                                     theatre_id INT NOT NULL,
                                     movie_id INT NOT NULL,
                                     FOREIGN KEY (theatre_id) REFERENCES theatre(id),
    FOREIGN KEY (movie_id) REFERENCES movie(id)
    );

CREATE TABLE IF NOT EXISTS reservation (
                                           id INT PRIMARY KEY,
                                           time_of_purchase TIMESTAMP NOT NULL,
                                           phone_number INT,
                                           email VARCHAR(255),
    show_id INT NOT NULL,
    FOREIGN KEY (show_id) REFERENCES shows(id)
    );

CREATE TABLE IF NOT EXISTS ticket (
                                      id INT PRIMARY KEY,
                                      reservation_id INT NOT NULL,
                                      FOREIGN KEY (reservation_id) REFERENCES reservation(id)
    );

CREATE TABLE IF NOT EXISTS booked_seats (
                                            id INT PRIMARY KEY,
                                            show_id INT NOT NULL,
                                            seat_id INT NOT NULL,
                                            ticket_id INT NOT NULL,
                                            FOREIGN KEY (show_id) REFERENCES shows(id),
    FOREIGN KEY (seat_id) REFERENCES seat(id),
    FOREIGN KEY (ticket_id) REFERENCES ticket(id)
    );

